#!/bin/bash
if [ -n "$1" ] && [ "$1" == "-task" ]; then
echo "Задание"
echo "11. Написать скрипт, который выводит 10 сообщений "в первом потоке прошла 1 сек" с периодичностью 1 с, и 5 сообщений с периодичностью 2 сек (использовать команду sleep n).."
echo
